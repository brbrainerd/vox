    out.push_str("        let conn = database.connect().expect(\"Failed to connect\");\n");
    out.push_str("        let subs = std::sync::Arc::new(vox_runtime::SubscriptionManager::new());\n");
    out.push_str("        Arc::new(AppDb { _guard: AppDbGuard::Local(database), conn, subs })\n");
    out.push_str("    };\n");

    // Generate SchemaDigest JSON and embed it for runtime migration
    let digest = crate::emit_main::generate_hir_digest(module);
